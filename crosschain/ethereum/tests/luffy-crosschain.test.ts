import {
  assert,
  describe,
  test,
  clearStore,
  beforeAll,
  afterAll
} from "matchstick-as/assembly/index"
import { BigInt, Address, Bytes } from "@graphprotocol/graph-ts"
import { BetAmountSet } from "../generated/schema"
import { BetAmountSet as BetAmountSetEvent } from "../generated/LuffyCrosschain/LuffyCrosschain"
import { handleBetAmountSet } from "../src/luffy-crosschain"
